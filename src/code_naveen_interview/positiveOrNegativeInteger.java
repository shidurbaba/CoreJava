public class positiveOrNegativeInteger {

    public static void main(String[] args) {
        double num = -12.33;

        if(num < 0.0)
        {
            System.out.println("negative num");
        } else if(num > 0.0)
        {
            System.out.println("positive num");
        } else
        {
            System.out.println("num is zero");
        }






    }
}
